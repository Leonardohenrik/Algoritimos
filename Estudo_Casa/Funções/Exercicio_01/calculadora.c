#include<stdio.h>


float somar(float num_1, float num_2){
    float soma;
    soma = num_1 + num_2;
    return soma;
}

float subtrair(float num_1, float num_2){
    float sub;
    sub = num_1 - num_2;
    return sub;
}

float multiplicar(float num_1, float num_2){
    float multi;
    multi = num_1 * num_2;
    return multi;
}

float dividir(float num_1, float num_2){
    float div;
    div = num_1 / num_2;
    return div;
}
